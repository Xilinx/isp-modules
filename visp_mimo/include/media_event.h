#ifndef __MEDIA_EVENT_H__
#define __MEDIA_EVENT_H__

#define MAX_DATA_SIZE (8 * 4096 - 64)

enum MediaEventId {
    MEDIA_EID_CREATE_PIPELINE = 0,
    MEDIA_EID_DESTROY_PIPELINE,
    MEDIA_EID_SET_FMT,
    MEDIA_EID_REQBUFS,
    MEDIA_EID_QBUF,
    MEDIA_EID_STREAMON,
    MEDIA_EID_STREAMOFF,
    MEDIA_EID_S_CTRL,
    MEDIA_EID_G_CTRL,
    MEDIA_EID_S_INTERVAL,
    MEDIA_EID_MAX,
};

typedef struct MediaCtrlAttr_s {
    uint32_t Cid;
    uint32_t DataSize;
    uint8_t  CtrlData[MAX_DATA_SIZE];
} MediaCtrlAttr;

#endif
